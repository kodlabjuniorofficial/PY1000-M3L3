print("--- Çift Sayıları Bulma ---")
# 1'den 20'ye kadar olan sayıları döngüye alalım.
for i in range(1, 21):
    # Her bir sayının 2'ye bölümünden kalan 0 mı diye kontrol edelim.
    if i % 2 == 0:
        # Eğer şart sağlanırsa, yani sayı çift ise ekrana yazdıralım.
        print(f"Bulunan çift sayı: {i}")

